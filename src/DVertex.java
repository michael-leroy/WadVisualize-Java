/**
 * Basic object to store WAD vertexes.
 */
public class DVertex {

    private int y;
    private int x;

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

}